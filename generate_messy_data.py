import pandas as pd
import numpy as np
import random 
from faker import Faker  #fake data generate krne ke liye use kiya h

fake = Faker() #variable name store kiya
np.random.seed(42)
random.seed(42)

N = 500

data = {
    'StudentID': list(range(1, N+1)),
    'Name': [fake.name() for _ in range(N)],
    'Age' : np.random.randint(15,20, N),
    'gender': np.random.choice(['M','F','Male','Female','m','f'],N),
    'Attendance': np.random.uniform(60,100,N),
    'AssignmentScore': np.random.uniform(40,100,N), #unifrom percentage score generate krne ke liye 
    'QuizScore': np.random.uniform(30,100,N),
    'MidtermScore': np.random.uniform(35,100,N),
    'FinalExamScore': np.random.uniform(40,100,N)
}

df = pd.DataFrame(data)

#inject messiness
missing_name_idx = np.random.choice(df.index, size=int(0.05*N), replace=False)
df.loc[missing_name_idx,'Name']= np.nan

duplicate_ids = np.random.choice(df['StudentID'],size=10,replace=False)
for dup_id in duplicate_ids:
    dup_row = df[df['StudentID']== dup_id].iloc[0].copy()
    dup_row['AssignmentScore']= dup_row['AssignmentScore'] * np.random.uniform(0.8,1.2)
    df = pd.concat([df, pd.DataFrame([dup_row])], ignore_index=True)

neg_idx = np.random.choice(df.index,size=int(0.02*N),replace = False)
df.loc[neg_idx, 'AssignmentScore']=np.random.uniform(-30,-1,len(neg_idx))
high_idx = np.random.choice(df.index, size=int(0.02*N),replace=False)
df.loc[high_idx,'FinalExamScore']=np.random.uniform(101,150, len(high_idx))

age_outliers = np.random.choice(df.index, size=int(0.02*N), replace=False)
df.loc[age_outliers, 'Age'] = np.random.choice([-5, 150, 200], len(age_outliers))

att_outliers = np.random.choice(df.index, size=int(0.03*N), replace=False)
df.loc[att_outliers, 'Attendance'] = np.random.choice([-10, 120, 200], len(att_outliers))

gender_mess = np.random.choice(df.index, size=int(0.04*N), replace=False)
df.loc[gender_mess, 'Gender'] = np.random.choice(
    ['X', 'unknown', 'male', 'FEMALE'],
    len(gender_mess)
)

quiz_neg = np.random.choice(df.index, size=int(0.02*N), replace=False)
df.loc[quiz_neg, 'QuizScore'] = np.random.uniform(-40, -1, len(quiz_neg))

df['Remarks'] = [
    fake.sentence() if random.random() > 0.1 else np.nan
    for _ in range(len(df))
]

df = df.sample(frac=1).reset_index(drop=True)

df.to_csv("messy_student_data.csv", index=False)

print(f"Messy data generated with {len(df)} records. Saved to messy_student_data.csv")



















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































